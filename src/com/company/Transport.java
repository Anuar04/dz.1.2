package com.company;

public class Transport {
    private Color color;
    private int year;
    double v1;

    public Transport(Color color, int year, double v1 ){
        this.color=color;
        this.year = year;
        this.v1 = this.v1;
    }

    public Color getColor() {
        return color;
    }

    public int getYear() {
        return year;
    }

    public double getV1() {
        return v1;
    }
}
